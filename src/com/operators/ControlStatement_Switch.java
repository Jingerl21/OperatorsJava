package com.operators;

public class ControlStatement_Switch {

	public static void main(String[] args) {
		int number = 20;
		switch (number) {
		
		case 19:
			System.out.println("15");
			break;
		case 10:
			System.out.println("10");
			break;
		case 20:
			System.out.println("Correct");
		
			break;
			
		}
	}
}